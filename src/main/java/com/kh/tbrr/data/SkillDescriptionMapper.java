        /**
         * 技能名から説明文を取得
         * 
         * @param skillName 技能名（日本語）
         * @return 説明文。見つからない場合は技能名をそのまま返す
         */
        public static String getDescription(String skillName) {
                return SKILL_DESCRIPTIONS.getOrDefault(skillName, skillName);
        }

        /**
         * 指定された技能名が説明文を持っているか確認
         * 
         * @param skillName 技能名
         * @return 説明文が存在する場合true
         */
        public static boolean hasDescription(String skillName) {
                return SKILL_DESCRIPTIONS.containsKey(skillName);
        }

        /**
         * すべての説明文を取得（読み取り専用）
         * 
         * @return 技能名と説明文のマップ
         */
        public static Map<String, String> getAllDescriptions() {
                return new HashMap<>(SKILL_DESCRIPTIONS);
        }
